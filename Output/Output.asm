.ORIG x3500
	;Store all the vars coming out of sqareroot
	ST	R3	TOTAL
	ST	R7	JUMPER
;Check to see if there is a number in the hundreds collumn
	LD 	R3	TOTAL
	LD	R2	HUND
	LD 	R7	DIVI
	JSRR 	R7
	;STORE REMAINDER
	ST 	R3,	REM
	ADD	R2,	R2,	#0
	BRnz	NZ
	;ADD ASCII CHANGER
	LD 	R4	ASCII
	ADD 	R0,	R2,	R4
	OUT
;Tens column
NZ	LD 	R3	REM
	LD	R2	TENS
	LD 	R7	DIVI
	JSRR 	R7
	;STORE REMAINDER
	ST 	R3,	REM
	ADD	R2,	R2,	#0
	BRnz	NTZ
	;ADD ASCII CHANGER
	LD 	R4	ASCII
	ADD 	R0,	R2,	R4
	OUT
;Output the last number
NTZ	LD 	R0	REM
	LD 	R4	ASCII
	ADD 	R0,	R0,	R4
	OUT
;OUTPUT THE SQAREROOT REMAINDER


	HALT
;VARS
ONES 	.FILL 	#1
TENS	.FILL	#10
HUND	.FILL	#100
DIVI	.FILL	x3000
JUMPER	.FILL	x0000
TOTAL	.FILL	#0
REM	.FILL	#0
DIV	.FILL	#0
ASCII	.FILL	#48
.END